import mongoose from 'mongoose';

const ShipperSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    tags: {
      type: [String],
    },
    industry: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    city: {
      type: String,
      required: true,
    },
    state: {
      type: String,
      required: true,
    },
    zip: {
      type: Number,
      required: true,
    },
    phone_number: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    website: {
      type: String,
    },
    annual_volume: {
      type: String,
    },
    annual_spend: {
      type: String,
    },
  },
  { timestamps: true }
);

const Shipper = mongoose.model("Shipper", ShipperSchema);
export default Shipper;